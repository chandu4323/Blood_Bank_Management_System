REM   Script: Blood Bank Management System
REM   DBMS project

CREATE TABLE Address 
( 
    Address_Id NUMBER GENERATED BY DEFAULT AS IDENTITY, 
    Street VARCHAR2(200), 
    City VARCHAR2(50), 
    Postal_Code NUMBER(10), 
    State VARCHAR2(100), 
    Country VARCHAR2(100), 
    PRIMARY KEY(Address_Id) 
);

CREATE TABLE Hospitals 
( 
    Hospital_Id NUMBER GENERATED BY DEFAULT AS IDENTITY, 
    Hospital_Name VARCHAR2(300), 
    Address_Id NUMBER NOT NULL, 
    Contact_Info NUMBER, 
    PRIMARY KEY(Hospital_Id), 
    FOREIGN KEY (Address_Id) References Address(Address_Id)  
);

CREATE TABLE Blood_Banks 
( 
    Blood_Bank_Id NUMBER GENERATED BY DEFAULT AS IDENTITY, 
    Blood_Bank_Name VARCHAR(200), 
    Address_Id NUMBER NOT NULL, 
    Contact_Info NUMBER, 
    PRIMARY KEY(Blood_Bank_Id), 
    FOREIGN KEY (Address_Id) References Address(Address_Id)  
);

CREATE TABLE Donors 
( 
    Donor_Id NUMBER GENERATED BY DEFAULT AS IDENTITY, 
    First_Name VARCHAR2(100), 
    Last_Name VARCHAR2(100), 
    Gender CHAR(2), 
    Age NUMBER, 
    Contact_Info NUMBER, 
    PRIMARY KEY(Donor_Id) 
);

CREATE TABLE Recipients 
( 
    Recipient_Id NUMBER GENERATED BY DEFAULT AS IDENTITY, 
    First_Name VARCHAR2(100), 
    Last_Name VARCHAR2(100), 
    Age NUMBER, 
    Gender CHAR(2), 
    Contact_Info NUMBER, 
    Hospital_Id NUMBER NOT NULL, 
    PRIMARY KEY(Recipient_Id), 
    FOREIGN KEY (Hospital_Id) References Hospitals(Hospital_Id)  
);

CREATE TABLE Camps 
( 
    Camp_Id NUMBER GENERATED BY DEFAULT AS IDENTITY, 
    Camp_Name VARCHAR2(300), 
    Event_Date DATE, 
    Address_Id NUMBER NOT NULL, 
    Contact_Info NUMBER, 
    PRIMARY KEY(Camp_Id), 
    FOREIGN KEY (Address_Id) References Address(Address_Id) 
);

CREATE TABLE Employee 
( 
    Employee_Id NUMBER GENERATED BY DEFAULT AS IDENTITY, 
    Name VARCHAR2(300), 
    Designation VARCHAR2(100), 
    Contact_Info NUMBER, 
    Blood_Bank_Id NUMBER NOT NULL, 
    PRIMARY KEY(Employee_Id), 
    FOREIGN KEY (Blood_Bank_Id) References Blood_Banks(Blood_Bank_Id) 
);

Create Table Requests 
( 
    Request_Id NUMBER GENERATED BY DEFAULT AS IDENTITY, 
    Hospital_Id NUMBER NOT NULL, 
    Blood_Bank_Id NUMBER NOT NULL, 
    Blood_Type Varchar2(50), 
    Request_Date DATE, 
    Quantity NUMBER, 
    Status VARCHAR2(50), 
    PRIMARY KEY(Request_Id), 
    FOREIGN KEY (Blood_Bank_Id) References Blood_Banks(Blood_Bank_Id), 
    FOREIGN KEY (Hospital_Id) References Hospitals(Hospital_Id) 
);

CREATE TABLE Donations 
( 
    Donation_Id NUMBER GENERATED BY DEFAULT AS IDENTITY, 
    Donor_Id NUMBER NOT NULL, 
    Camp_Id NUMBER, 
    Date_Time TIMESTAMP, 
    PRIMARY KEY(Donation_Id), 
    FOREIGN KEY (Donor_Id) References Donors(Donor_Id), 
    FOREIGN KEY (Camp_Id) References Camps(Camp_Id) 
);

CREATE TABLE Tests 
( 
    Test_Id NUMBER GENERATED BY DEFAULT AS IDENTITY, 
    Donation_Id NUMBER NOT NULL, 
    Test_Date DATE, 
    Test_Results VARCHAR2(50), 
    PRIMARY KEY(Test_Id), 
    FOREIGN KEY (Donation_Id) References Donations(Donation_Id) 
);

CREATE TABLE Blood_Stock 
( 
    Blood_Unit_Id NUMBER GENERATED BY DEFAULT AS IDENTITY, 
    Blood_Type VARCHAR2(10), 
    Received_Date DATE, 
    Expiration_Date DATE, 
    Units NUMBER, 
    Blood_Bank_Id NUMBER NOT NULL, 
    Test_Id NUMBER NOT NULL, 
    PRIMARY KEY(Blood_Unit_Id), 
    FOREIGN KEY (Blood_Bank_Id) References Blood_Banks(Blood_Bank_Id), 
    FOREIGN KEY (Test_Id) References Tests(Test_Id) 
);

